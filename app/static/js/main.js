/**
 * Show Bootstrap Toast
 * @function showToast
 * @param {String} message
 * @param {String} bsClass
 * @param {String} delay
 */
function show_toast(message, bsClass = 'success', delay = '6000') {
    // console.debug(`show_toast: ${message}`)
    if (isAndroid && Android.showToast) {
        console.log('Android Toast')
        Android.showToast(
            typeof message === 'string' ? message : message.text()
        )
        return
    }

    let element = $('#toast').clone()
    element.removeAttr('id').addClass(`text-bg-${bsClass}`)
    const body = element.find('.toast-body')
    if (typeof message === 'string') {
        body.text(message)
    } else {
        body.append(message)
    }
    element.toast({ delay: Number.parseInt(delay) })
    element.appendTo('.toast-container').toast('show')
}

/**
 * Save Options
 * @function saveOptions
 * @param {InputEvent} event
 */
// eslint-disable-next-line no-unused-vars
function saveOptions(event) {
    const excludes = ['data-bs-theme-value']
    if (excludes.includes(event.target.id)) {
        return console.debug('ignored setting:', event.target.id)
    }
    console.debug(`saveOptions: ${event.type}`, event)
    const form = $(this)
    const data = new FormData(this)
    // console.debug('data:', data)
    $.ajax({
        type: 'post',
        url: globalThis.location.pathname,
        data: data,
        headers: { 'X-CSRFToken': csrftoken },
        success: success,
        error: error,
        cache: false,
        contentType: false,
        processData: false,
    })
    function error(jqXHR) {
        formErrorHandler.call(this, form, jqXHR)
    }
    function success(data) {
        console.debug('success:', data, event)
        if (data.reload) {
            location.reload()
        } else {
            // let message = 'Settings Saved Successfully.'
            // show_toast(message, 'success')
            // TODO: Improve handling for other types and add to this if
            if (event.target.type === 'text') {
                event.target.classList.add('is-valid')
                setTimeout(
                    () => event.target.classList.remove('is-valid'),
                    3000
                )
            }
        }
    }
}

/**
 * Error Message responseJSON.error or jqXHR.statusText
 * @param {jQuery.jqXHR} jqXHR
 */
function messageErrorHandler(jqXHR) {
    if (jqXHR.responseJSON?.error) {
        const message = `${jqXHR.status}: ${jqXHR.responseJSON.error}`
        show_toast(message, 'danger')
    } else {
        const message = `${jqXHR.status}: ${jqXHR.statusText}`
        show_toast(message, 'danger')
    }
}

/**
 * Loop through form errors and display them
 * formErrorHandler.call(this, form, jqXHR)
 * @param {jQuery} form
 * @param {jQuery.jqXHR} jqXHR
 */
function formErrorHandler(form, jqXHR) {
    if (jqXHR.status === 400) {
        let data = jqXHR.responseJSON
        console.log('jqXHR.responseJSON data:', data)
        $(form.prop('elements')).each(function () {
            if (Object.hasOwn(data, this.name)) {
                $(`#${this.name}-invalid`).empty().append(data[this.name])
                $(this).addClass('is-invalid')
            }
        })
    }
    const message = `${jqXHR.status}: ${jqXHR.statusText}`
    show_toast(message, 'danger')
}

/**
 * Debounce Function
 * @function debounce
 * @param {Function} fn
 * @param {Number} timeout
 */
function debounce(fn, timeout = 250) {
    let timeoutID
    return (...args) => {
        clearTimeout(timeoutID)
        timeoutID = setTimeout(() => fn(...args), timeout)
    }
}

// Toolbar items collapsed one tier at a time, least- to most-aggressive, as
// soon as the row would otherwise wrap. A width breakpoint can't drive this:
// the wrap point depends on which buttons are visible (list vs. gallery,
// album vs. not), so it's measured instead (see observeToolbar below).
const TOOLBAR_COLLAPSE_TIERS = [
    'files-toolbar--hide-count',
    'files-toolbar--hide-select-all-label',
    'files-toolbar--hide-bulk-label',
    'files-toolbar--hide-sort-label',
    'files-toolbar--hide-size-label',
    'files-toolbar--hide-view-labels', // List/Gallery/Map/Slideshow go icon-only...
    'files-toolbar--collapse-views', // ...then unify into one icon+popup (files-view-toggle-btn)
]

/**
 * One ResizeObserver per toolbar, driving both the --toolbar-h CSS var and
 * the collapse tiers above — two observers on the same element would double
 * the layout work per resize for no benefit.
 */
function observeToolbar(toolbarId, cssVar) {
    const toolbar = document.getElementById(toolbarId)
    if (!toolbar) return
    const firstRowItem = toolbar.querySelector(
        ':scope > :not(.files-toolbar-album-row)'
    )
    const actions = toolbar.querySelector('.files-toolbar-actions')
    // Sibling buttons on the same row can differ by a px or two from
    // padding/line-height, so only a full row's worth of offset counts.
    const isWrapped = () =>
        !!firstRowItem &&
        !!actions &&
        actions.getBoundingClientRect().top -
            firstRowItem.getBoundingClientRect().top >
            10

    const sync = () => {
        document.documentElement.style.setProperty(
            cssVar,
            `${toolbar.offsetHeight}px`
        )
        toolbar.classList.remove(...TOOLBAR_COLLAPSE_TIERS)
        for (const tier of TOOLBAR_COLLAPSE_TIERS) {
            if (!isWrapped()) break
            toolbar.classList.add(tier)
        }
    }
    sync()
    // Also fires when content (not just viewport width) changes how many
    // buttons need to fit — e.g. switching list -> gallery view adds
    // Sort/Display/Select-all without any window resize.
    new ResizeObserver(sync).observe(toolbar)
}

// eslint-disable-next-line no-unused-vars
function initToolbar(toolbarId, dt) {
    const searchInputId = `${toolbarId}-search-input`
    if (dt) wireToolbarSearch(searchInputId, dt)
    initCollapsibleSearch(`${toolbarId}-search`, searchInputId)
    observeToolbar(toolbarId, '--toolbar-h')
}

function wireToolbarSearch(inputId, dt) {
    const input = document.getElementById(inputId)
    if (!input || !dt) return
    let timer
    input.addEventListener('input', () => {
        clearTimeout(timer)
        timer = setTimeout(() => dt.search(input.value).draw(), 300)
    })
}

function initCollapsibleSearch(wrapperId, inputId) {
    const wrapper = document.getElementById(wrapperId)
    const input = document.getElementById(inputId)
    if (!wrapper || !input) return

    const expand = () => {
        wrapper.classList.add('files-toolbar-search--expanded')
        input.focus()
    }
    const collapse = () => {
        if (!input.value)
            wrapper.classList.remove('files-toolbar-search--expanded')
    }

    wrapper.addEventListener('click', (e) => {
        if (!wrapper.classList.contains('files-toolbar-search--expanded')) {
            e.stopPropagation()
            expand()
        }
    })
    input.addEventListener('blur', collapse)
}

// eslint-disable-next-line no-unused-vars
async function initDataTable(dt, skeletonFn, fetchFn, emptyMsg, zeroMsg) {
    skeletonFn()
    // Freeze auto-width so each draw(false) inside fetchFn doesn't shift columns.
    dt.settings()[0].oFeatures.bAutoWidth = false
    await fetchFn()
    // Hide tbody before DT redraws real rows so the skeleton→real swap is invisible.
    // dtRevealThead (called by the caller after us) fades it back in.
    const tbody = dt.table().node().querySelector(':scope > tbody')
    if (tbody) {
        tbody.style.opacity = '0'
        tbody.style.transition = 'none'
    }
    initDtLang(dt, emptyMsg, zeroMsg)
    if (!dt.rows().count()) dt.draw()
}

function initDtLang(dt, emptyMsg, zeroMsg) {
    const lang = dt.settings()[0].oLanguage
    lang.sEmptyTable = emptyMsg
    lang.sZeroRecords = zeroMsg
}

/**
 * Build skeleton placeholder rows into a DataTable tbody.
 * @param {HTMLElement} tbody
 * @param {number} count
 * @param {Array<{w: number, h?: number}>} specs - per-column width/height;
 *   w: 0 marks a flexible slot that absorbs leftover row width (the fill
 *   column, e.g. name), keeping the fixed blocks column-aligned across rows
 * @param {Object<number, number[]>} varWidths - colIndex -> percentage widths
 *   cycled per row for the shimmer inside a flexible (w: 0) slot
 */
function buildSkeletonRows(tbody, count, specs, varWidths = {}) {
    tbody
        .querySelectorAll('td.dt-empty')
        .forEach((cell) => cell.closest('tr')?.remove())
    tbody.querySelectorAll('.dt-skeleton-row').forEach((el) => el.remove())
    const vis = columnVisibility(tbody.closest('table'))
    const colSpan = vis.filter(Boolean).length || 1
    const fragment = document.createDocumentFragment()
    for (let i = 0; i < count; i++) {
        const tr = document.createElement('tr')
        tr.className = 'dt-skeleton-row'
        const td = document.createElement('td')
        td.colSpan = colSpan
        const track = document.createElement('div')
        track.className = 'dt-skeleton-track'
        specs.forEach(({ w, h = 14 }, colIndex) => {
            const cell = document.createElement('div')
            cell.className = 'dt-skeleton-cell'
            cell.style.height = `${h}px`
            const widths = varWidths[colIndex]
            let block = cell
            if (w === 0) {
                block = document.createElement('div')
                block.className = 'dt-skeleton-flex'
                cell.style.width = `${widths ? widths[i % widths.length] : 100}%`
                block.appendChild(cell)
            } else {
                cell.style.width = `${w}px`
            }
            block.dataset.col = colIndex
            if (vis[colIndex] === false) block.style.display = 'none'
            track.appendChild(block)
        })
        td.appendChild(track)
        tr.appendChild(td)
        fragment.appendChild(tr)
    }
    tbody.appendChild(fragment)
}

// Skeleton rows use a single td spanning every visible column (with
// max-width: 0 in table.css) so they are geometrically inert: they can never
// add phantom columns or feed fake content widths into the browser's table
// layout. DataTables responsive hides columns through several mechanisms
// (inline styles, the dtr-hidden class, header colspan adjustments), so the
// thead's computed display is the one reliable signal. Skeleton blocks carry
// data-col so they can be eliminated exactly like the real columns.
function columnVisibility(table) {
    const ths = table?.querySelectorAll(':scope > thead > tr > th')
    const vis = []
    ths?.forEach((th) => vis.push(getComputedStyle(th).display !== 'none'))
    return vis
}

// Responsive may hide/show columns while skeletons are on screen; re-sync
// colspans and block visibility after the resize settles so skeleton rows
// keep matching the real column grid.
window.addEventListener(
    'resize',
    debounce(() => {
        const tables = new Set()
        document
            .querySelectorAll('tr.dt-skeleton-row')
            .forEach((tr) => tables.add(tr.closest('table')))
        tables.forEach((table) => {
            const vis = columnVisibility(table)
            const span = vis.filter(Boolean).length || 1
            table
                .querySelectorAll(':scope > tbody > .dt-skeleton-row > td')
                .forEach((td) => (td.colSpan = span))
            table
                .querySelectorAll('.dt-skeleton-track > [data-col]')
                .forEach((el) => {
                    el.style.display =
                        vis[el.dataset.col] === false ? 'none' : ''
                })
        })
    }, 250),
    { passive: true }
)

