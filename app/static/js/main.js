/**
 * Initialize Document
 * @function domContentLoaded
 */
function domContentLoaded() {
    console.log('domContentLoaded: main.js')
    // Show any toast generated by template on load
    $('.toast-alert').each(function () {
        if (isAndroid && Android.showToast) {
            Android.showToast(this.querySelector('.toast-body').textContent)
        } else {
            const toastAlert = new bootstrap.Toast($(this))
            toastAlert.show()
        }
    })
    if (isAndroid) {
        console.log('Android Detected.')
        if (Android.receiveAuthToken) {
            setTimeout(getAuthToken, 250)
        }
    }

    const parser = new UAParser()
    const ua = parser.getResult()
    console.log('ua:', ua)
    if (
        !ua.ua.includes('DjangoFiles') &&
        ['mobile', 'tablet'].includes(ua.device?.type)
    ) {
        console.log(`%cMobile Client Detected`, 'color: Lime')
        const navItem = document.getElementById('mobileAuthNav')
        navItem?.classList.remove('d-none')
        const link = navItem?.querySelector('a')
        link?.addEventListener('click', processAuth)
    }
}

