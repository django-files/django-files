/**
 * Show Bootstrap Toast
 * @function showToast
 * @param {String} message
 * @param {String} bsClass
 * @param {String} delay
 */
function show_toast(message, bsClass = 'success', delay = '6000') {
    // console.debug(`show_toast: ${message}`)
    if (isAndroid && Android.showToast) {
        console.log('Android Toast')
        Android.showToast(
            typeof message === 'string' ? message : message.text()
        )
        return
    }

    let element = $('#toast').clone()
    element.removeAttr('id').addClass(`text-bg-${bsClass}`)
    const body = element.find('.toast-body')
    if (typeof message === 'string') {
        body.text(message)
    } else {
        body.append(message)
    }
    element.toast({ delay: Number.parseInt(delay) })
    element.appendTo('.toast-container').toast('show')
}

/**
 * Save Options
 * @function saveOptions
 * @param {InputEvent} event
 */
// eslint-disable-next-line no-unused-vars
function saveOptions(event) {
    const excludes = ['data-bs-theme-value']
    if (excludes.includes(event.target.id)) {
        return console.debug('ignored setting:', event.target.id)
    }
    console.debug(`saveOptions: ${event.type}`, event)
    const form = $(this)
    const data = new FormData(this)
    // console.debug('data:', data)
    $.ajax({
        type: 'post',
        url: globalThis.location.pathname,
        data: data,
        headers: { 'X-CSRFToken': csrftoken },
        success: success,
        error: error,
        cache: false,
        contentType: false,
        processData: false,
    })
    function error(jqXHR) {
        formErrorHandler.call(this, form, jqXHR)
    }
    function success(data) {
        console.debug('success:', data, event)
        if (data.reload) {
            location.reload()
        } else {
            // let message = 'Settings Saved Successfully.'
            // show_toast(message, 'success')
            // TODO: Improve handling for other types and add to this if
            if (event.target.type === 'text') {
                event.target.classList.add('is-valid')
                setTimeout(
                    () => event.target.classList.remove('is-valid'),
                    3000
                )
            }
        }
    }
}

/**
 * Error Message responseJSON.error or jqXHR.statusText
 * @param {jQuery.jqXHR} jqXHR
 */
function messageErrorHandler(jqXHR) {
    if (jqXHR.responseJSON?.error) {
        const message = `${jqXHR.status}: ${jqXHR.responseJSON.error}`
        show_toast(message, 'danger')
    } else {
        const message = `${jqXHR.status}: ${jqXHR.statusText}`
        show_toast(message, 'danger')
    }
}

/**
 * Loop through form errors and display them
 * formErrorHandler.call(this, form, jqXHR)
 * @param {jQuery} form
 * @param {jQuery.jqXHR} jqXHR
 */
function formErrorHandler(form, jqXHR) {
    if (jqXHR.status === 400) {
        let data = jqXHR.responseJSON
        console.log('jqXHR.responseJSON data:', data)
        $(form.prop('elements')).each(function () {
            if (Object.hasOwn(data, this.name)) {
                $(`#${this.name}-invalid`).empty().append(data[this.name])
                $(this).addClass('is-invalid')
            }
        })
    }
    const message = `${jqXHR.status}: ${jqXHR.statusText}`
    show_toast(message, 'danger')
}

/**
 * Debounce Function
 * @function debounce
 * @param {Function} fn
 * @param {Number} timeout
 */
function debounce(fn, timeout = 250) {
    let timeoutID
    return (...args) => {
        clearTimeout(timeoutID)
        timeoutID = setTimeout(() => fn(...args), timeout)
    }
}

function observeToolbarHeight(toolbarId, cssVar) {
    const toolbar = document.getElementById(toolbarId)
    const container = toolbar?.parentElement
    if (!toolbar || !container) return
    const sync = () =>
        container.style.setProperty(cssVar, `${toolbar.offsetHeight}px`)
    sync()
    new ResizeObserver(sync).observe(toolbar)
}

// eslint-disable-next-line no-unused-vars
function initToolbar(toolbarId, dt) {
    const searchInputId = `${toolbarId}-search-input`
    if (dt) wireToolbarSearch(searchInputId, dt)
    initCollapsibleSearch(`${toolbarId}-search`, searchInputId)
    observeToolbarHeight(toolbarId, '--toolbar-h')
}

function wireToolbarSearch(inputId, dt) {
    const input = document.getElementById(inputId)
    if (!input || !dt) return
    let timer
    input.addEventListener('input', () => {
        clearTimeout(timer)
        timer = setTimeout(() => dt.search(input.value).draw(), 300)
    })
}

function initCollapsibleSearch(wrapperId, inputId) {
    const wrapper = document.getElementById(wrapperId)
    const input = document.getElementById(inputId)
    if (!wrapper || !input) return

    const expand = () => {
        wrapper.classList.add('files-toolbar-search--expanded')
        input.focus()
    }
    const collapse = () => {
        if (!input.value)
            wrapper.classList.remove('files-toolbar-search--expanded')
    }

    wrapper.addEventListener('click', (e) => {
        if (!wrapper.classList.contains('files-toolbar-search--expanded')) {
            e.stopPropagation()
            expand()
        }
    })
    input.addEventListener('blur', collapse)
}

// eslint-disable-next-line no-unused-vars
async function initDataTable(dt, skeletonFn, fetchFn, emptyMsg, zeroMsg) {
    skeletonFn()
    // Freeze auto-width so each draw(false) inside fetchFn doesn't shift columns.
    dt.settings()[0].oFeatures.bAutoWidth = false
    await fetchFn()
    initDtLang(dt, emptyMsg, zeroMsg)
    if (!dt.rows().count()) dt.draw()
}

function initDtLang(dt, emptyMsg, zeroMsg) {
    const lang = dt.settings()[0].oLanguage
    lang.sEmptyTable = emptyMsg
    lang.sZeroRecords = zeroMsg
}

/**
 * Build skeleton placeholder rows into a DataTable tbody.
 * @param {HTMLElement} tbody
 * @param {number} count
 * @param {Array<{w: number, h?: number}>} specs - per-column width/height
 * @param {Object<number, number[]>} varWidths - colIndex -> array of widths cycled per row
 */
function buildSkeletonRows(tbody, count, specs, varWidths = {}) {
    tbody
        .querySelectorAll('td.dt-empty')
        .forEach((cell) => cell.closest('tr')?.remove())
    tbody.querySelectorAll('.dt-skeleton-row').forEach((el) => el.remove())
    const fragment = document.createDocumentFragment()
    for (let i = 0; i < count; i++) {
        const tr = document.createElement('tr')
        tr.className = 'dt-skeleton-row'
        specs.forEach(({ w, h = 14 }, colIndex) => {
            const td = document.createElement('td')
            const cell = document.createElement('div')
            cell.className = 'dt-skeleton-cell'
            const widths = varWidths[colIndex]
            const width = widths ? widths[i % widths.length] : w
            cell.style.width = `${width}px`
            cell.style.height = `${h}px`
            td.appendChild(cell)
            tr.appendChild(td)
        })
        fragment.appendChild(tr)
    }
    tbody.appendChild(fragment)
}

/**
 * Submit a modal form as JSON via jQuery AJAX.
 * @param {jQuery} form
 * @param {Function} onSuccess
 */
// eslint-disable-next-line no-unused-vars
function submitJsonForm(form, onSuccess) {
    const data = new FormData(form[0])
    data.forEach((value, key) => (data[key] = value))
    $.ajax({
        type: form.attr('method'),
        url: form.attr('action'),
        data: JSON.stringify(data),
        headers: { 'X-CSRFToken': csrftoken },
        success: onSuccess,
        error: messageErrorHandler,
        cache: false,
        contentType: false,
        processData: false,
    })
}

async function pageScroll(event, nextPage, callable, buffer = 500) {
    // await sleep(200)
    const maxScrollY = document.body.scrollHeight - globalThis.innerHeight
    console.debug(
        `pageScroll: ${globalThis.scrollY} > ${maxScrollY - buffer}`,
        globalThis.scrollY > maxScrollY - buffer
    )
    if (nextPage && (!maxScrollY || globalThis.scrollY > maxScrollY - buffer)) {
        console.debug('End of Scroll')
        await callable()
    }
}

/**
 * Viewport-based dynamic char-length tracker. Use for column-text truncation
 * that should respond to window resizes. Same formula file-table.js has used
 * for file names: round(slope * innerWidth + 8).
 *
 *   const truncator = createTruncator(0.04)
 *   truncator.attach(myDataTable)
 *   // in a render fn:  txt.slice(0, truncator.length)
 *
 * Calls dt.rows().invalidate('data').draw(false) on resize so already-rendered
 * cells re-flow to the new length.
 *
 * @param {number} slope per-pixel char growth — tighten on half-width cards.
 * @returns {{ attach: (dt: any) => void, length: number }}
 */
// eslint-disable-next-line no-unused-vars
function createTruncator(slope = 0.04) {
    let dt = null
    const compute = () => Math.round(slope * window.innerWidth + 8)
    let len = compute()
    window.addEventListener(
        'resize',
        debounce(() => {
            len = compute()
            if (dt) dt.rows().invalidate('data').draw(false)
        }, 100),
        { passive: true }
    )
    return {
        attach(table) {
            dt = table
        },
        get length() {
            return len
        },
    }
}

/**
 * Manages nextPage / fetchLock / row insertion for an infinite-scroll DataTable
 * fed by a paginated API. Pass `maxRows` for home-dashboard-style cards that
 * cap at e.g. 10 rows and surface a truncation hint via `onOverflow`.
 *
 *   const loader = createPaginatedLoader(dt, {
 *       fetcher: fetchShorts,        // (page) => { next, shorts: [...] }
 *       listKey: 'shorts',           // key of the array inside the response
 *       idPrefix: 'short',           // DT_RowId = `${idPrefix}-${row.id}`
 *       countEl: totalShortsCount,   // optional <strong> to update
 *       maxRows: isHome ? 10 : null,
 *       onOverflow: () => warn.classList.remove('d-none'),
 *   })
 *   await loader.load()
 *   loader.reset()                   // before re-fetching from page 1
 *
 * @param {any} dt DataTables API instance.
 * @param {object} opts
 * @returns {{ load: () => Promise<void>, addRow: (row: any) => void, reset: () => void, nextPage: any }}
 */
// eslint-disable-next-line no-unused-vars
function createPaginatedLoader(dt, opts) {
    let nextPage = 1
    let lock = false

    function addRow(row) {
        row['DT_RowId'] = `${opts.idPrefix}-${row.id}`
        dt.row.add(row).draw(false)
        if (opts.countEl) opts.countEl.textContent = dt.rows().count()
    }

    async function load() {
        if (lock) return
        if (opts.maxRows && dt.rows().count() >= opts.maxRows) {
            nextPage = null
            return
        }
        lock = true
        try {
            const data = await opts.fetcher(nextPage)
            nextPage = data.next
            const list = data[opts.listKey] || []
            let added = 0
            for (const row of list) {
                if (opts.maxRows && dt.rows().count() >= opts.maxRows) break
                addRow(row)
                added += 1
            }
            if (opts.maxRows) {
                if (list.length > added || !!data.next) opts.onOverflow?.()
                nextPage = null
            }
        } finally {
            lock = false
        }
    }

    function reset() {
        nextPage = 1
        lock = false
    }

    return {
        load,
        addRow,
        reset,
        get nextPage() {
            return nextPage
        },
    }
}

/**
 * Wires the standard scroll + resize infinite-scroll triggers for a loader
 * created by createPaginatedLoader.
 */
// eslint-disable-next-line no-unused-vars
function attachInfiniteScroll(dt, loader) {
    const handle = debounce(async (event) => {
        await pageScroll(event, loader.nextPage, loader.load)
        dt?.columns.adjust().draw()
    })
    document.addEventListener('scroll', handle)
    window.addEventListener('resize', handle)
}

/**
 * Generate Random String at length
 * @param {Number} length
 * @return {String}
 */
function genRand(length) {
    const chars =
        'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789'
    let result = ''
    let counter = 0
    while (counter < length) {
        const rand = Math.floor(Math.random() * chars.length)
        result += chars.charAt(rand)
        counter += 1
    }
    return result
}

